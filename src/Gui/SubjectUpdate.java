    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
    if (evt.getClickCount()==1){
        
            jTable1.setEnabled(false);
            
              int selectdeRow = jTable1.getSelectedRow();
              
                String subjectNumber = String.valueOf(jTable1.getValueAt(selectdeRow, 0));
            jLabel3.setText(subjectNumber);
            
             String subjectName = String.valueOf(jTable1.getValueAt(selectdeRow, 1));
            jTextField2.setText(subjectName);
            
           String description = String.valueOf(jTable1.getValueAt(selectdeRow, 2));
            jTextArea1.setText(description);
            
            String price = String.valueOf(jTable1.getValueAt(selectdeRow, 3));
            jTextField1.setText(price);
    }
    }//GEN-LAST:event_jTable1MouseClicked

