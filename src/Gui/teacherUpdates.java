    private void loadTeachers(){
      try{
   ResultSet teacherResult = mysql.execute("SELECT * FROM `teacher` INNER JOIN `gender` ON `teacher`.`Gender_gender_id`=`gender`.`gender_id`"
   +"INNER JOIN `subject` ON `teacher`.`Subject_Subno`=`subject`.`Subno`"
   +"INNER JOIN `teachers_address` ON `teacher`.`teachers_address_id`=`teachers_address`.`id`");
   
       DefaultTableModel model =(DefaultTableModel)jTable1.getModel();
            model.setRowCount(0);
            
            while(teacherResult.next()){
                
            Vector teacher = new Vector();
            
         teacher.add(teacherResult.getString("Tno"));
         teacher.add(teacherResult.getString("First_Name"));
         teacher.add(teacherResult.getString("Last_Name"));
         teacher.add(teacherResult.getString("Mobile"));
         teacher.add(teacherResult.getString("gender"));
         teacher.add(teacherResult.getString("id"));
         teacher.add(teacherResult.getString("Address_line1"));
         teacher.add(teacherResult.getString("Address_line2"));
               
                        model.addRow(teacher);
                  jTable1.setModel(model);
            }
    }catch(Exception e){
            e.printStackTrace();
        }
      
  }
    
