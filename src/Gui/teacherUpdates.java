    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int selectedRow = jTable1.getSelectedRow();
                if(selectedRow==0){
                JOptionPane.showMessageDialog(this, "Please Select Row",
                               "WARNING", JOptionPane.WARNING_MESSAGE);
                }
         String Teacherno =String.valueOf(jTable1.getValueAt(selectedRow, 0));
         
          try{
             
                mysql.execute("DELETE FROM `class` "
                        +"WHERE `Teacher_Tno`='"+Teacherno+"'");
                
               mysql.execute("DELETE FROM`teacher`"
               +"WHERE `Tno`='"+Teacherno+"'");
               
                           
             JOptionPane.showMessageDialog(this, "Teacher Remove SuccessFull",
                               "information", JOptionPane.INFORMATION_MESSAGE);
             
            reset();
            loadTeachers();
             jTable1.setEnabled(true);
             
               }catch(Exception e){
            e.printStackTrace();
          }
    }//GEN-LAST:event_jButton5ActionPerformed

