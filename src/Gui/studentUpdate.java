       String AddressId = jLabel13.getText();
       String AddressLine1 = jTextField6.getText();
       String AddressLine2 = jTextField4. getText();
   
       
      
        try{
            
               mysql.execute("UPDATE `student` SET"
               +"`First_Name`='"+FirstName+"',`Last_Name`='"+lastName+"',`Mobile`='"+Mobile+"'"
               +"WHERE `Sno`='"+studentNumber+"'");
               
                mysql.execute("UPDATE `address` SET"
               +"`adress_line1`='"+AddressLine1+"',`adress_line2`='"+AddressLine2+"'"
               +"WHERE `adress_id`='"+AddressId+"'");
               
                           
             JOptionPane.showMessageDialog(this, "Student Update SuccessFull",
                               "information", JOptionPane.INFORMATION_MESSAGE);
             
             reset();
             loadStudent();
             jTable1.setEnabled(true);
            
           }catch(Exception e){
            e.printStackTrace();
        }
        
       
